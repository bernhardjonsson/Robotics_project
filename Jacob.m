function J = Jacob(T01, T12, T23, T34)

z01 = [T01(1,3) T01(2,3) T01(2,3)]
o01 = [T01(1,4) T01(2,4) T01(2,4)]

z12 = [T12(1,3) T12(2,3) T12(2,3)]
o12 = [T12(1,4) T12(2,4) T12(2,4)]

z23 = [T23(1,3) T23(2,3) T23(2,3)]
o23 = [T23(1,4) T23(2,4) T23(2,4)]

z34 = [T34(1,3) T34(2,3) T34(2,3)]
o34 = [T34(1,4) T34(2,4) T34(2,4)]



T04=T01*T12*T23*T34;
T14=T12*T23*T34;
T24=T23*T34;


o04 = [T04(1,4) T04(2,4) T04(2,4)]
o14 = [T14(1,4) T14(2,4) T14(2,4)]
o24 = [T24(1,4) T24(2,4) T24(2,4)]


Jv1 = cross(z01,(o04-o01))
Jw1 = z01
Jv2 = cross(z12,(o14-o12))
Jw2 = z12
Jv3 = cross(z23,(o24-o23))
Jw3 = z23
Jv4 = cross(z34,(o34-o34))
Jw4 = z34

J1 = [Jv1 Jw1]';
J2 = [Jv2 Jw2]';
J3 = [Jv3 Jw3]';
J4 = [Jv4 Jw4]';


J = [J1 J2 J3 J4]

end